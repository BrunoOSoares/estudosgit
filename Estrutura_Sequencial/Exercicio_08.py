#Faça um Programa que pergunte quanto você ganha por hora e o numero de horas trabalhadas no mês. Calcule e mostre o total do seu salario no referido mês.

valor_hora = float(input("Informa o valor da hora: "))

hora_trabalhada = int(input("Informa o numero de horas trabalhadas no mês: "))

salario = valor_hora * hora_trabalhada

print("O salario do mês é : ", salario)

